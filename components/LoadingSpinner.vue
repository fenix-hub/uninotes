<template>
  <div 
    class="animate-spin rounded-full border-2 border-gray-300 border-t-blue-600"
    :class="sizeClass"
  />
</template>

<script setup lang="ts">
interface Props {
  size?: 'sm' | 'md' | 'lg'
}

const props = withDefaults(defineProps<Props>(), {
  size: 'md'
})

const sizeClass = computed(() => {
  const sizes = {
    sm: 'w-4 h-4',
    md: 'w-6 h-6', 
    lg: 'w-8 h-8'
  }
  return sizes[props.size]
})
</script>
